<div id="accordion">
          <!-- Question 1 -->
    	<h3>How does a Medicare Supplement Plan work?</h3>
        <div>
          <p>These plans work hand-in-hand with traditional Medicare by supplementing your Part A and Part B benefits — they fill in some of the gaps between what Medicare pays and what you are expected to pay (deductibles, copayments and coinsurance). Depending on the plan you choose, you will have little to no out-of-pocket expenses for Medicare-covered services.</p>
        </div>
        <!-- Question 2 -->
    	<h3>What's the difference between Medicare Supplement and Medicare Advantage plans?</h3>
        <div>
        <p>Although both are good options for people with Medicare, there are very big differences between these two types of insurance.
        </p>
        <ul>
<li><b>A Medicare Supplement plan — or Medigap policy —</b> is private insurance you can purchase to cover some or most of your out-of-pocket expenses accrued through traditional Medicare. These may include Part B costs, like the 20% you'd otherwise pay for physician visits and other outpatient services, the Part A hospital deductible (currently $1,260 for each hospital benefit period) and the Part B deductible. With a Medigap policy, you're free to use any doctors and hospitals that accept Medicare and you do not need referrals from a primary care doctor.</li>
<li><b>Medicare Advantage plans — most often HMOs and PPOs — </b>are offered through private health plans and are an alternative to traditional Medicare. Every plan must cover all the same benefits as traditional Medicare and many plans offer extra benefits such as vision, fitness and prescription coverage. Most plans require you to go to doctors and other providers within their service network (or with a PPO, pay higher copays for going out of network). Referrals from a primary care doctor are required in most cases.</li></ul>
</div>
        <!-- Question 3 -->
          	<h3>Can my spouse and I be covered under the same Medigap policy?</h3>
        <div>
          <p>If you and your spouse both want Medigap coverage, you'll each have to buy separate policies. A Medigap policy only covers one person.</p>
        </div>
        <!-- Question 4 -->
        <h3>Can I have both a Medigap policy and a Medicare Advantage plan?</h3>
        <div>
          <p>If you enroll in a Medicare Advantage health plan you cannot, by law, also purchase a Medigap policy, even though you still have out-of-pocket expenses under that health plan.</p>
        </div>
         <!-- Question 5 -->
        <h3>Are prescription drugs covered?</h3>
        <div>
          <p>Some Medigap policies sold in the past cover prescription drugs, but Medigap policies sold after January 1, 2006 aren't allowed to include prescription drug coverage. If you want Medicare Part D drug coverage, you can join a Medicare Prescription Drug Plan (PDP). For a list of Medicare-approved PDPs in your area, visit <a href="http://www.medicare.gov" class="external" target="_blank">www.medicare.gov</a>.</p>
        </div>
		<!-- Question 6 -->
    	<h3>Am I covered outside of Massachusetts?</h3>
        <div>
          <p>Absolutely. With a Tufts Medicare Preferred Supplement plan, you can travel with confidence. Your coverage is good throughout the U.S. — everywhere Medicare is accepted — just like it is in Massachusetts. Emergency care is covered worldwide.</p>
        </div>        
        <!-- Question 7 -->
        <h3>Do I have to change doctors?</h3>
        <div>
          <p>There is no need to change doctors. You can see the doctors you’re most comfortable with as long as they accept Medicare. You don’t have to use a special network or pick a primary care doctor — and you don’t need referrals to see a specialist.</p>
        </div> 
        <!-- Question 8 -->
        <h3>Do I have to choose my doctors from a network?</h3>
        <div>
          <p>No. With a Tufts Medicare Preferred Supplement plan, you can use any doctor, anywhere and at any time. There are no networks. Your coverage is good everywhere Medicare is accepted.</p>
        </div>
        <!-- Question 9 -->
        <h3>Do I have to file claims?</h3>
        <div>
          <p>In most cases, claims are filed for you automatically, saving you the time and trouble.</p>
        </div>
        <!-- Question 10 -->
        <h3>Will I have a copay when I see the doctor?</h3>
        <div>
          <p>You won't. With Tufts Medicare Preferred Supplement plans, doctor visits are covered 100% (although with Tufts Medicare Preferred Supplement Core you must first satisfy your annual Part B deductible — which is approximately $100).</p>
        </div>
        <!-- Question 11 -->
        <h3>How do I receive a reimbursement for joining a fitness club?</h3>
        <div>
          <p>Our fitness club reimbursement is part of our commitment to helping you lead an active lifestyle. All Tufts Medicare Preferred Supplement plan members are eligible to receive up to $150 per calendar year toward club membership fees and/or exercise classes by enrolling with a qualified health club or fitness facility. Nutritional counseling also qualifies for the reimbursement. <a href="http://www.tuftsmedicarepreferred.org/sites/default/files/docs_2014/H2256_2013_238_Wellness_Benefit_form.pdf" class="external" target="_blank">Click here to download the reimbursement form</a>.</p>
        </div>
        <!-- Question 12 -->
        <h3>How do I know if a health club or fitness facility qualifies for the reimbursement?</h3>
        <div>
          <p>Qualified health clubs and fitness facilities provide cardiovascular and strength-training exercise equipment on site. Examples include traditional health clubs and community fitness centers; YMCAs, YWCAs and Jewish Community Centers with a fitness facility on site; and Tufts Health Plan Medicare Preferred network of fitness centers in Massachusetts — Curves&reg; and Fitness Together.</p>
        </div>
